package EstructurasDeControl;

public class If_Else {
    /**
     * if-else: Permite ejecutar un bloque de código si una condición es verdadera y otro bloque de código si la
     * condición es falsa. También se puede utilizar una estructura if-else anidada para evaluar múltiples condiciones.
     */
    public static void main(String[] args) {
        int num = 10;
        if (num > 0) {
            System.out.println("El número es positivo");
        } else {
            System.out.println("El número es negativo");
        }

    }
}
