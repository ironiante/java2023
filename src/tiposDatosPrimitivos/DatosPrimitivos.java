package tiposDatosPrimitivos;

public class DatosPrimitivos {
    /**
     * Tipos de datos primitivos:
     *
     * byte: representa valores enteros de 8 bits.
     * short: representa valores enteros de 16 bits.
     * int: representa valores enteros de 32 bits.
     * long: representa valores enteros de 64 bits.
     * float: representa valores de punto flotante de 32 bits.
     * double: representa valores de punto flotante de 64 bits.
     * boolean: representa un valor booleano (verdadero o falso).
     * char: representa un único carácter Unicode.
     * Tipo de dato String: representa una secuencia de caracteres. A diferencia de los tipos de datos primitivos, String es una clase en Java.
     *
     * Otros tipos de datos:
     *
     * Arrays: permiten almacenar colecciones de elementos del mismo tipo.
     * Clases y Objetos: en Java, también puedes crear tus propios tipos de datos utilizando clases y objetos.
     */
    public static void main(String[] args) {
        // Variables primitivas
        int edad = 25;
        double altura = 1.75;
        boolean esEstudiante = true;
        char inicial = 'J';
        // String
        String nombre = "Juan";



    }
}
